export function supportedLanguageCodes() {
  return Object.keys(LANGUAGE_CONFIG).filter((language) => TRANSLATIONS[language]);
}

export function normalizeLanguage(language) {
  return supportedLanguageCodes().includes(language) ? language : DEFAULT_LANGUAGE;
}

export function languageConfig(language) {
  const code = normalizeLanguage(language);
  const config = LANGUAGE_CONFIG[code] || {};

  return {
    label: code.toUpperCase(),
    htmlLang: code,
    intlLocale: code,
    plotlyLocaleName: `harvest-${code}`,
    ...config
  };
}

export function translate(language, key, values = {}) {
  const code = normalizeLanguage(language);
  const dictionary = TRANSLATIONS[code] || TRANSLATIONS[DEFAULT_LANGUAGE];
  const fallback = TRANSLATIONS[DEFAULT_LANGUAGE][key] || key;

  return String(dictionary[key] || fallback).replace(/\{(\w+)\}/g, (_, name) =>
    values[name] === undefined ? "" : String(values[name])
  );
}
